package io.github.jiangdequan;

public class PrintNumbers {
        public static void main(String[] args) {
            int i = 1; // Start at 1
            
            // Use a while loop to print numbers from 1 to 20
            while (i <= 20) {
                System.out.println(i);
                i++; // Increment the counter
            }
        }
    }