package io.github.jiangdequan;

public class LogicalOperators {


        public static void main(String[] args) {
            // Logical AND (&&)
            System.out.println(10 > 5 && 5 < 20);  // Output: true
    
            // Logical OR (||)
            System.out.println(10 < 5 || 5 < 20);  // Output: true
    
            // Logical NOT (!)
            System.out.println(!(10 > 5));  // Output: false
        }
    }