package io.github.jiangdequan;

public class StringConcatenation {

        public static void main(String[] args) {
            // Define two strings
            String str1 = "Hello";
            String str2 = "World";
    
            // Concatenate using the + operator
            String result = str1 + " " + str2;
    
            // Print the result
            System.out.println("Concatenated String: " + result);
        }
    }